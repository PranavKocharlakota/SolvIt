/** Detect whether a string looks like a math formula. */
export function isFormula(text: string): boolean {
  return /[=+\-×÷/^²³√πθαβμσωΔΣλ∞∫∂≈<>()]/.test(text) ||
    /\d/.test(text) && /[a-zA-Z]/.test(text);
}

/**
 * Convert a string to an array of path DrawPrimitives using the stroke font.
 * @param text      The string to render
 * @param x         Left edge in 0-100 canvas coordinates
 * @param y         Baseline y in 0-100 canvas coordinates (top of character)
 * @param charH     Character height in 0-100 canvas units
 * @param color     Stroke color
 * @param spacing   Extra gap between characters (fraction of charH, default 0.15)
 */
export function textToPaths(
  text: string,
  x: number,
  y: number,
  charH: number,
  color: string = '#1a1a2e',
  spacing: number = 0.15,
): DrawPrimitive[] {
  const charW = charH * 0.62;
  const gap = charH * spacing;
  const strokeW = Math.max(0.3, charH * 0.09);

  const prims: DrawPrimitive[] = [];
  let cx = x;

  for (const ch of text) {
    const glyphStrokes = FONT[ch];
    if (glyphStrokes === undefined) {
      // Unknown character — skip but still advance cursor
      cx += charW * 0.5 + gap;
      continue;
    }

    for (const stroke of glyphStrokes) {
      if (stroke.length < 2) continue;
      prims.push({
        type: 'path',
        points: stroke.map(([nx, ny]) => ({
          x: cx + nx * charW,
          y: y + ny * charH,
        })),
        color,
        width: strokeW,
      });
    }

    cx += charW + gap;
  }

  return prims;
}

/** Estimate the pixel width (in 0-100 canvas units) of a string at given charH. */
export function measureText(text: string, charH: number, spacing = 0.15): number {
  const charW = charH * 0.62;
  const gap = charH * spacing;
  return text.length * (charW + gap) - gap;
}
